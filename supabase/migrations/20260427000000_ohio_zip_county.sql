-- Ohio ZIP -> County crosswalk (HUD USPS Q1 2023, Ohio FIPS 39).
-- 1,359 unique ZIPs, 88 counties, 2,056 ZIP-county pairs.
-- Use res_ratio + is_primary_county for clean lookups.
-- =====================================================================

CREATE TABLE IF NOT EXISTS public.ohio_zip_county (
    zip                TEXT    NOT NULL,
    county_fips        TEXT    NOT NULL,
    county_name        TEXT    NOT NULL,
    usps_city          TEXT,
    usps_state         TEXT    NOT NULL DEFAULT 'OH',
    res_ratio          NUMERIC(6,4) NOT NULL DEFAULT 0,
    bus_ratio          NUMERIC(6,4) NOT NULL DEFAULT 0,
    oth_ratio          NUMERIC(6,4) NOT NULL DEFAULT 0,
    tot_ratio          NUMERIC(6,4) NOT NULL DEFAULT 0,
    is_primary_county  BOOLEAN NOT NULL DEFAULT FALSE,
    PRIMARY KEY (zip, county_fips)
);

CREATE INDEX IF NOT EXISTS idx_ohio_zip_county_zip          ON public.ohio_zip_county (zip);
CREATE INDEX IF NOT EXISTS idx_ohio_zip_county_primary_zip  ON public.ohio_zip_county (zip) WHERE is_primary_county;
CREATE INDEX IF NOT EXISTS idx_ohio_zip_county_county_name  ON public.ohio_zip_county (county_name);

ALTER TABLE public.ohio_zip_county ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "ohio_zip_county_read_all" ON public.ohio_zip_county;
CREATE POLICY "ohio_zip_county_read_all"
    ON public.ohio_zip_county FOR SELECT
    USING (TRUE);

-- View: one-row-per-ZIP primary county (the common case for donor/voter lookups)
CREATE OR REPLACE VIEW public.ohio_zip_primary_county AS
SELECT zip, county_fips, county_name, usps_city, usps_state
FROM public.ohio_zip_county
WHERE is_primary_county = TRUE;

-- Helper function: returns the primary county for a ZIP, normalising input.
CREATE OR REPLACE FUNCTION public.county_for_zip(p_zip TEXT)
RETURNS TEXT
LANGUAGE sql
STABLE
AS $$
    SELECT county_name
    FROM public.ohio_zip_county
    WHERE zip = LPAD(REGEXP_REPLACE(COALESCE(p_zip, ''), '[^0-9]', '', 'g'), 5, '0')
      AND is_primary_county = TRUE
    LIMIT 1;
$$;

