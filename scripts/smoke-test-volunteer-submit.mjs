#!/usr/bin/env node
/**
 * scripts/smoke-test-volunteer-submit.mjs
 * --------------------------------------
 * End-to-end smoke test for /.netlify/functions/volunteer-submit.
 *
 * What it does:
 *   1. POSTs a fake volunteer signup, asserts ok=true and gets back the row id.
 *   2. POSTs a fake intern application, asserts ok=true and gets back the row id.
 *   3. Re-reads both rows out of Supabase using the service-role key, asserts
 *      the columns we care about made it in unchanged.
 *   4. (Optional, --cleanup) deletes the two test rows so the admin tables
 *      stay clean.
 *
 * Usage:
 *   ENDPOINT=https://www.ohiopride.org/.netlify/functions/volunteer-submit \
 *   SUPABASE_URL=https://dkdxefzhttkmjhdbkvqn.supabase.co \
 *   SUPABASE_SERVICE_ROLE_KEY=<service_role_key> \
 *   node scripts/smoke-test-volunteer-submit.mjs --cleanup
 *
 * Exit code:
 *   0 on success, 1 on any assertion failure.
 */

import { createClient } from '@supabase/supabase-js';

const ENDPOINT = process.env.ENDPOINT
  || 'https://www.ohiopride.org/.netlify/functions/volunteer-submit';
const SUPABASE_URL = process.env.SUPABASE_URL;
const SERVICE_ROLE = process.env.SUPABASE_SERVICE_ROLE_KEY;
const CLEANUP = process.argv.includes('--cleanup');

if (!SUPABASE_URL || !SERVICE_ROLE) {
  console.error('Missing SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY in env.');
  process.exit(1);
}

const supabase = createClient(SUPABASE_URL, SERVICE_ROLE, {
  auth: { persistSession: false },
});

const stamp = Date.now();
const TEST_EMAIL_VOL    = `smoke+vol+${stamp}@ohiopride.test`;
const TEST_EMAIL_INTERN = `smoke+intern+${stamp}@ohiopride.test`;

// --------------------------------------------------------------------------
// 1. Volunteer payload
// --------------------------------------------------------------------------
const volunteerPayload = {
  application_type: 'volunteer',
  first_name: 'Smoke', last_name: `Volunteer ${stamp}`,
  email: TEST_EMAIL_VOL,
  phone: '513-555-0100',
  pronouns: 'they/them',
  city: 'Cincinnati', county: 'Hamilton', zip: '45202',
  registered_voter: 'yes',
  interests: ['field_canvassing','phone_text_banking','social_amplification'],
  skills: ['writing','data_analysis'],
  availability: ['weekends','weekday_evenings'],
  time_commitment: 'monthly',
  prior_campaign_experience: true,
  prior_campaign_notes: 'Smoke test — please ignore.',
  referral_source: 'smoke-test',
  is_founding_member: false,
  additional_notes: 'This row was created by smoke-test-volunteer-submit.mjs',
  email_optin: true,
  sms_optin: false,
};

// --------------------------------------------------------------------------
// 2. Intern payload
// --------------------------------------------------------------------------
const internPayload = {
  application_type: 'internship',
  first_name: 'Smoke', last_name: `Intern ${stamp}`,
  email: TEST_EMAIL_INTERN,
  phone: '513-555-0101',
  pronouns: 'she/her',
  city: 'Columbus', county: 'Franklin', zip: '43215',
  position: 'legislative_internship',
  term: 'summer_2026',
  start_date_pref: 'June 2',
  weekly_hours: 12,
  credit_hours: 3,
  institution: 'The Ohio State University',
  program_major: 'Political Science',
  class_year: 'Senior',
  faculty_sponsor_name: 'Prof. Smoke Test',
  faculty_sponsor_email: 'faculty+smoke@ohiopride.test',
  resume_url: 'https://example.com/smoke-resume.pdf',
  portfolio_url: null,
  statement_of_interest:
    'Smoke test statement of interest. Please ignore. Generated by smoke-test-volunteer-submit.mjs.',
  prior_experience: 'Two years of student government.',
  why_ohio_pride: 'Smoke test record.',
  email_optin: true,
};

// --------------------------------------------------------------------------
// Helpers
// --------------------------------------------------------------------------
const FAILS = [];
function pass(msg) { console.log('  PASS  ' + msg); }
function fail(msg) { console.log('  FAIL  ' + msg); FAILS.push(msg); }
function eq(label, got, want) {
  if (Array.isArray(want)) {
    const a = (got || []).slice().sort();
    const b = want.slice().sort();
    if (JSON.stringify(a) !== JSON.stringify(b)) {
      return fail(`${label}: got ${JSON.stringify(got)} want ${JSON.stringify(want)}`);
    }
  } else if (got !== want) {
    return fail(`${label}: got ${JSON.stringify(got)} want ${JSON.stringify(want)}`);
  }
  pass(label);
}

async function postJson(payload) {
  const res = await fetch(ENDPOINT, {
    method: 'POST',
    headers: { 'content-type': 'application/json' },
    body: JSON.stringify(payload),
  });
  let body = {};
  try { body = await res.json(); } catch {}
  return { status: res.status, body };
}

// --------------------------------------------------------------------------
// 3. Run it
// --------------------------------------------------------------------------
async function main() {
  console.log('Endpoint:    ' + ENDPOINT);
  console.log('Supabase:    ' + SUPABASE_URL);
  console.log('Cleanup:     ' + (CLEANUP ? 'yes' : 'no'));
  console.log('');

  console.log('[1/4] POST volunteer payload');
  const vRes = await postJson(volunteerPayload);
  eq('  HTTP 200',                vRes.status,           200);
  eq('  body.ok === true',        vRes.body.ok,          true);
  eq('  body.kind === volunteer', vRes.body.kind,        'volunteer');
  if (!vRes.body.id) fail('  body.id is null/empty');
  else pass('  body.id present (' + vRes.body.id + ')');
  console.log('');

  console.log('[2/4] POST intern payload');
  const iRes = await postJson(internPayload);
  eq('  HTTP 200',                  iRes.status,    200);
  eq('  body.ok === true',          iRes.body.ok,   true);
  eq('  body.kind === internship',  iRes.body.kind, 'internship');
  if (!iRes.body.id) fail('  body.id is null/empty');
  else pass('  body.id present (' + iRes.body.id + ')');
  console.log('');

  console.log('[3/4] Re-read volunteer row from Supabase');
  const v = await supabase.from('volunteers').select('*').eq('email', TEST_EMAIL_VOL).maybeSingle();
  if (v.error || !v.data) {
    fail('  could not re-read volunteer row: ' + (v.error?.message || 'not found'));
  } else {
    pass('  row found');
    eq('  first_name',       v.data.first_name,       volunteerPayload.first_name);
    eq('  last_name',        v.data.last_name,        volunteerPayload.last_name);
    eq('  email',            v.data.email,            TEST_EMAIL_VOL.toLowerCase());
    eq('  county',           v.data.county,           'Hamilton');
    eq('  zip',              v.data.zip,              '45202');
    eq('  registered_voter', v.data.registered_voter, 'yes');
    eq('  interests',        v.data.interests,        volunteerPayload.interests);
    eq('  skills',           v.data.skills,           volunteerPayload.skills);
    eq('  time_commitment',  v.data.time_commitment,  'monthly');
    eq('  status (default)', v.data.status,           'new');
  }
  console.log('');

  console.log('[4/4] Re-read intern row from Supabase');
  const i = await supabase.from('intern_applications').select('*').eq('email', TEST_EMAIL_INTERN).maybeSingle();
  if (i.error || !i.data) {
    fail('  could not re-read intern row: ' + (i.error?.message || 'not found'));
  } else {
    pass('  row found');
    eq('  first_name',       i.data.first_name,             internPayload.first_name);
    eq('  email',            i.data.email,                  TEST_EMAIL_INTERN.toLowerCase());
    eq('  position',         i.data.position,               'legislative_internship');
    eq('  term',             i.data.term,                   'summer_2026');
    eq('  weekly_hours',     i.data.weekly_hours,           12);
    eq('  credit_hours',     Number(i.data.credit_hours),   3);
    eq('  institution',      i.data.institution,            'The Ohio State University');
    eq('  resume_url',       i.data.resume_url,             internPayload.resume_url);
    eq('  statement length', i.data.statement_of_interest?.length > 20, true);
    eq('  status (default)', i.data.status,                 'new');
  }
  console.log('');

  if (CLEANUP) {
    console.log('Cleanup: deleting smoke-test rows');
    const dv = await supabase.from('volunteers').delete().eq('email', TEST_EMAIL_VOL);
    const di = await supabase.from('intern_applications').delete().eq('email', TEST_EMAIL_INTERN);
    if (dv.error) console.log('  WARN  could not delete volunteer row: ' + dv.error.message);
    if (di.error) console.log('  WARN  could not delete intern row: '    + di.error.message);
    if (!dv.error && !di.error) console.log('  Deleted both test rows.');
    console.log('');
  } else {
    console.log('Skipping cleanup (pass --cleanup to also delete the smoke-test rows).');
    console.log('Test rows are visible in /admin/volunteers and /admin/internships under:');
    console.log('  ' + TEST_EMAIL_VOL);
    console.log('  ' + TEST_EMAIL_INTERN);
    console.log('');
  }

  if (FAILS.length === 0) {
    console.log('ALL CHECKS PASSED');
    process.exit(0);
  } else {
    console.log(`FAILED (${FAILS.length})`);
    for (const f of FAILS) console.log('  - ' + f);
    process.exit(1);
  }
}

main().catch(function (err) {
  console.error('Smoke test crashed:', err);
  process.exit(1);
});
